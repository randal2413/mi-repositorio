let sumarNumero = parseInt(prompt("Ingresar numero para sumar"))

for (let index =0; index <=10 ; index++) {
    let suma = sumarNumero + index
    alert(sumarNumero + "+" + index +"="+ suma)

}

let restarNumero = parseInt(prompt("Ingresar numero para restar"))


for (let index = 0; index <=10 ; index++) {
    let resta = index - restarNumero
    alert(restarNumero + "-" + index +"="+ resta)

}

